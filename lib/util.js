/**
 * Returns the `value` parameter.
 *
 * @param {*} value
 * @return {*}
 */
export const returnValue = (value) => value;

/**
 * Gets the difference between two numbers.
 *
 * @param {Number} n1
 * @param {Number} n2
 * @return {Number}
 */
export const getNumberDifference = (n1, n2) => Math.abs(n1 - n2);
